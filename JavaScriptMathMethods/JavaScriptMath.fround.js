/* JavaScript Math.fround()

Definition and Usage
The Math.fround() method returns the nearest 32-bit single precision float representation of a number.

Syntax
Math.fround(x)

Return Value
Type	    Description
Number	    The nearest 32-bit single precision float representation of the number.

Example */

console.log(Math.fround(2.5222399999046325684));
console.log(Math.fround(2.50));
console.log(Math.fround(2.49));
console.log(Math.fround(-2.60));
console.log(Math.fround(-2.50));
console.log( Math.fround(-2.49));